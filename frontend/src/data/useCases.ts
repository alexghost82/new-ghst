// ── Visual identity for the demo camera viewport ──────────────────────────────
// Maps 1:1 to the `SCENES` lookup in `UseCasesPage`. Keep keys in sync.
export type SceneKey = "night" | "day" | "indoor" | "dock" | "cold";

// The three monitoring modes Ghost runs for every zone.
export type CheckKind = "periodic" | "critical" | "scheduled";

export interface Zone {
  name: string;
  // One natural-language check per monitoring mode.
  checks: Record<CheckKind, string>;
}

export interface SectorDemo {
  scene: SceneKey;
  zone: string;
  time: string;
  image?: string;
  // The operator question and Ghost's answer shown in the "in practice" demo.
  question: string;
  answer: string;
}

