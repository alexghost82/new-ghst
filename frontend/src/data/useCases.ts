// The first nine entries are the featured/flagship sectors (see note above the
// SECTORS declaration). Mark them so the Use Cases page can surface them in the
// dedicated primary band.
for (const sector of SECTORS.slice(0, 9)) {
  sector.tier = "primary";
}

export function getSector(id: string): Sector | undefined {
  return SECTORS.find((s) => s.id === id);
}

// ── Localization ──────────────────────────────────────────────────────────────
// The sector library is authored in Hebrew (the primary market). `LocalizedSector`
// mirrors `Sector` and is what the Use Cases page renders. English copy is not
// authored per-sector yet, so `localizeSector` currently returns the source
// sector unchanged for every locale — callers that need the raw record (e.g. for
// camera labels) keep using the original `Sector`.
export type SectorZone = Zone;

export type LocalizedSector = Sector;

export function localizeSector(
  sector: Sector,
  _locale: Locale,
): LocalizedSector {
  return sector;
}
